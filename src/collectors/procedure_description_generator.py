def validate_descriptions() -> list[str]:
    """説明文の文字数を検証する。200-300字の範囲外があればエラーリストを返す。"""
    errors = []
    for name, desc in PROCEDURE_DESCRIPTIONS.items():
        char_count = len(desc)
        if char_count < 180:  # 少し余裕を持って下限チェック
            errors.append(f"  ⚠ {name}: {char_count}字（短すぎる可能性）")
        elif char_count > 320:  # 少し余裕を持って上限チェック
            errors.append(f"  ⚠ {name}: {char_count}字（長すぎる可能性）")
    return errors


def main():
    """メイン処理: 説明文のバリデーション→DB更新"""
    db_path = os.path.join(os.path.dirname(__file__), "..", "..", "data", "aura.db")
    db_path = os.path.normpath(db_path)

    print(f"📦 データベース: {db_path}")
    print(f"📝 説明文: {len(PROCEDURE_DESCRIPTIONS)}件準備済み")
    print()

    # バリデーション
    print("=== 説明文バリデーション ===")
    errors = validate_descriptions()
    if errors:
        print("文字数の注意点:")
        for e in errors:
            print(e)
    else:
        print("✅ 全ての説明文が適正な文字数です")

    for name, desc in PROCEDURE_DESCRIPTIONS.items():
        print(f"  {name}: {len(desc)}字")
    print()

    # DB接続
    conn = sqlite3.connect(db_path)
    cur = conn.cursor()

    # 現在のprocedures一覧を取得
    cur.execute("SELECT id, name, description FROM procedures")
    procedures = cur.fetchall()
    print(f"=== DB内の施術: {len(procedures)}件 ===")

    # マッチング確認
    db_names = {row[1] for row in procedures}
    script_names = set(PROCEDURE_DESCRIPTIONS.keys())

    missing_in_script = db_names - script_names
    missing_in_db = script_names - db_names

    if missing_in_script:
        print(f"⚠ DBにあるがスクリプトにない施術: {missing_in_script}")
    if missing_in_db:
        print(f"⚠ スクリプトにあるがDBにない施術: {missing_in_db}")
    if not missing_in_script and not missing_in_db:
        print("✅ 全施術が一致しています")
    print()

    # UPDATE実行
    print("=== description更新 ===")
    updated_count = 0
    skipped_count = 0

    for proc_id, proc_name, current_desc in procedures:
        if proc_name in PROCEDURE_DESCRIPTIONS:
            new_desc = PROCEDURE_DESCRIPTIONS[proc_name]
            cur.execute(
                "UPDATE procedures SET description = ? WHERE id = ?",
                (new_desc, proc_id),
            )
            if cur.rowcount > 0:
                updated_count += 1
                print(f"  ✅ 更新: {proc_name} ({len(new_desc)}字)")
            else:
                skipped_count += 1
                print(f"  ⚠ 更新失敗: {proc_name}")
        else:
            skipped_count += 1
            print(f"  ⏭ スキップ（説明文なし）: {proc_name}")

    conn.commit()
    print()

    # 検証
    print("=== 更新後の検証 ===")
    cur.execute("SELECT COUNT(*) FROM procedures WHERE description IS NOT NULL AND description != ''")
    filled_count = cur.fetchone()[0]
    cur.execute("SELECT COUNT(*) FROM procedures")
    total_count = cur.fetchone()[0]

    print(f"  説明文あり: {filled_count}/{total_count}件")
    print(f"  更新件数: {updated_count}件")
    print(f"  スキップ: {skipped_count}件")

    if filled_count == total_count:
        print("  ✅ 全施術に説明文が設定されました")
    else:
        print(f"  ⚠ {total_count - filled_count}件の施術に説明文がありません")

    conn.close()
    print()
    print("✅ 完了")


if __name__ == "__main__":
    main()
