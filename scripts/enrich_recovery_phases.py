def main():
    """28施術のrecovery_phasesを投入"""
    db = sqlite3.connect(DB_PATH)
    updated = 0
    errors = []

    for name, phases in RECOVERY_DATA.items():
        json_str = json.dumps(phases, ensure_ascii=False)
        cursor = db.execute(
            "UPDATE procedures SET recovery_phases = ? WHERE name = ?",
            (json_str, name)
        )
        if cursor.rowcount > 0:
            updated += 1
            print(f"✅ {name} — {len(phases)}フェーズ投入")
        else:
            errors.append(name)
            print(f"❌ {name} — レコード未検出")

    db.commit()
    db.close()

    print(f"\n=== 完了: {updated}/28施術にrecovery_phases投入 ===")
    if errors:
        print(f"エラー: {errors}")


if __name__ == "__main__":
    main()
